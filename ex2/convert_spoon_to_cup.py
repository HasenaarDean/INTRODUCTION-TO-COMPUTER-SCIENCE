# Magic Numbers:

SPOONS_IN_ONE_CUP = 3.5


def convert_spoon_to_cup(number_of_spoons):
    """this function converts spoons 
    number to cups number"""

    return number_of_spoons / SPOONS_IN_ONE_CUP
