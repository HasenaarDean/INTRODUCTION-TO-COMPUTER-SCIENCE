import math


def golden_ratio():
    """This function prints the golden ratio"""
    print((math.sqrt(5) + 1) / 2)


def square_five():
    """This function prints the result of five by the power of two"""
    print(5 ** 2)


def hypotenuse():
    """This function prints the length of the hypotenuse of a right triangle
    which its edges are 4 and 5 length"""
    print(math.sqrt(4 ** 2 + 5 ** 2))


def pi():
    """This function prints the value of the number pi"""
    print(math.pi)


def e():
    """This function prints the value of the number e"""
    print(math.e)


def squares_area():
    """This function prints the squares' areas in a graded length of edges
    from 1 to 10"""
    print(1 ** 2, 2 ** 2, 3 ** 2, 4 ** 2, 5 ** 2, 6 ** 2, 7 ** 2, 8 ** 2,
          9 ** 2, 10 ** 2)